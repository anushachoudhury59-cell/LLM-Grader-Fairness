def main():
    with open("sampled_tickets.csv") as f:
        tickets = list(csv.DictReader(f))
    assert len(tickets) == 20, f"expected 20 sampled tickets, got {len(tickets)}"
    assert set(REPLIES.keys()) == set(range(1, 21)), "REPLIES must cover ticket_id 1-20"

    rows = []
    for t in tickets:
        ticket_id = int(t["ticket_id"])
        customer_message = "Hi, this is {{NAME}}. " + t["raw_customer_text"]
        good_reply, bad_reply = REPLIES[ticket_id]
        for quality, answer_text in [("good", good_reply), ("bad", bad_reply)]:
            rows.append(
                {
                    "unit_id": f"t{ticket_id:02d}_{quality}",
                    "source_category": t["source_category"],
                    "ticket_id": ticket_id,
                    "quality": quality,
                    "customer_message": customer_message,
                    "answer_text": answer_text,
                }
            )

    assert len(rows) == 40, f"expected 40 base units, got {len(rows)}"

    fieldnames = ["unit_id", "source_category", "ticket_id", "quality", "customer_message", "answer_text"]
    with open("base_units.csv", "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"Wrote {len(rows)} rows to base_units.csv")


if __name__ == "__main__":
    main()
